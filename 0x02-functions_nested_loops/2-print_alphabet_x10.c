#include "main.h"
/**
 * print lowercase alphabet 10 times
 * Description: You are not allowed to include standard libraries
 * Return: 0
 */
void print_alphabet_x10(void)
{
	char ch;
	int i;

	i = 0;

	while (i < 10)
	{
		ch = 'a';
		while (ch <= 'z')
		{
			_putchar(ch);
			ch++;
		}
		_putchar('\n');
		i++;
	}
}
